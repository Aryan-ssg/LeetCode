import java.util.ArrayList;
import java.util.Arrays;
import java.util.HashSet;
import java.util.List;
import java.util.Set;

class Solution {
    public List<List<Integer>> permuteUnique(int[] nums) {
        List<List<Integer>> result=new ArrayList<>();
       
        permutations(0,nums,result);
        return result;
        
    }
    static void combinations1(int index,int[] nums,List<List<Integer>> result){
        if(index==nums.length-1){
            ArrayList<Integer> arr=new ArrayList<>();
            for(int i=0;i<nums.length;i++){
                arr.add(nums[i]);
            }
            result.add(arr);
            return;
        }
        for(int i=index;i<nums.length;i++){
            if(nums[i]==nums[index] && i!=index){
                continue;
            }
            swap(nums,i,index);
            combinations1(index+1, nums,result);
            swap(nums,i,index);
            
        }
    }
    static void swap(int[] nums,int i,int index){
        int temp=nums[i];
        nums[i]=nums[index];
        nums[index]=temp;
    }



    static void permutations(int index,int[] nums,List<List<Integer>> result){
        if(index==nums.length-1){
            List<Integer> arr=new ArrayList<>();
            for(int i=0;i<nums.length;i++){
                arr.add(nums[i]);
            }
            result.add(arr);
            return;
        }     
        Set<Integer> used=new HashSet<>();
        for(int i=index;i<nums.length;i++){
            if(used.contains(nums[i])){
                continue;
            }
            used.add(nums[i]);
            swap(nums,i,index);
            permutations(index+1,nums,result);
            swap(nums,i,index);
        }
    }
}
